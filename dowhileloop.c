#include<stdio.h>
int main()
{
    int i = 1; //Declare and initialize variable i with value 1.

    do // Start of do-while loop.
    {
        printf("%d\n", i ); // Print the current value of i.
        i++; // increase the value of i by 1.
    }
    while(i <= 5); // Check condition after executing the loop body.
                    // Loop continues until i becomes greater than 5
    return 0 ;
}





